"""Lightweight readiness benchmark.

Runs deterministic checks against a project to score how prepared it is for safe agentic
coding. Does *not* evaluate model intelligence; only checks observable artifacts that the
scaffold itself can produce or that the project should have.

CLI:
    coding-scaffold eval init    - write `.coding-scaffold/eval-config.json` (optional)
    coding-scaffold eval run     - execute all enabled checks, write the result, return the report
    coding-scaffold eval report  - print the most recent report (re-runs by default)

Checks call into other modules (`context_lint`, `mcp`, `permissions`, etc.) as pure functions —
no shell, no network, no LLM.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path

from .context import DEFAULT_MAX_CONTEXT_TOKENS, inspect_context_budget
from .context_lint import lint_context
from .mcp import MCP_CONFIG_SOURCES, MCP_POLICY_RELATIVE
from .permissions import PERMISSIONS_RELATIVE
from .pr_template import PR_TEMPLATE_RELATIVE
from .session import SESSIONS_DIR


EVAL_CONFIG_RELATIVE = Path(".coding-scaffold") / "eval-config.json"
EVAL_REPORT_RELATIVE = Path(".coding-scaffold") / "eval-report.json"

# Recognized build/test/lint signal files.
BUILD_SIGNAL_FILES: tuple[str, ...] = (
    "pyproject.toml",
    "package.json",
    "Cargo.toml",
    "go.mod",
    "Gemfile",
    "composer.json",
    "CMakeLists.txt",
    "Makefile",
    "makefile",
)

LINT_SIGNAL_FILES: tuple[str, ...] = (
    "pyproject.toml",  # ruff/black config typically lives here
    ".ruff.toml",
    "ruff.toml",
    ".eslintrc",
    ".eslintrc.json",
    ".eslintrc.js",
    "eslint.config.js",
    ".rubocop.yml",
    "rustfmt.toml",
)


@dataclass(frozen=True)
class EvalCheck:
    name: str
    category: str
    passed: bool
    message: str
    detail: dict[str, object] | None = None

    def to_dict(self) -> dict[str, object]:
        payload: dict[str, object] = {
            "name": self.name,
            "category": self.category,
            "passed": self.passed,
            "message": self.message,
        }
        if self.detail is not None:
            payload["detail"] = self.detail
        return payload


@dataclass(frozen=True)
class EvalReport:
    checks: list[EvalCheck] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)

    @property
    def passed_count(self) -> int:
        return sum(1 for c in self.checks if c.passed)

    @property
    def total_count(self) -> int:
        return len(self.checks)

    @property
    def score(self) -> float:
        if not self.checks:
            return 0.0
        return self.passed_count / self.total_count

    def to_dict(self) -> dict[str, object]:
        return {
            "checks": [c.to_dict() for c in self.checks],
            "warnings": list(self.warnings),
            "score": round(self.score, 4),
            "passed": self.passed_count,
            "total": self.total_count,
        }


def default_eval_config() -> dict[str, object]:
    return {
        "$schema_version": 1,
        "description": (
            "Generated by `coding-scaffold eval init`. Toggle individual checks here. "
            "Set a check to false to skip it during `eval run`."
        ),
        "checks": {
            "build_command_detected": True,
            "test_command_detected": True,
            "lint_command_detected": True,
            "agent_instructions_exist": True,
            "policy_exists": True,
            "denied_files_configured": True,
            "pr_template_exists": True,
            "mcp_policy_exists_if_mcp_detected": True,
            "session_trace_template_exists": True,
            "context_lint_clean": True,
            "context_budget_acceptable": True,
        },
        "context_budget_max_tokens": DEFAULT_MAX_CONTEXT_TOKENS,
    }


def write_eval_config(target: Path, *, force: bool = False) -> dict[str, object]:
    root = target.expanduser().resolve()
    path = root / EVAL_CONFIG_RELATIVE
    if path.exists() and not force:
        return {"path": str(path), "created": False, "skipped": True}
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        json.dumps(default_eval_config(), indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    return {"path": str(path), "created": True, "skipped": False}


def run_eval(target: Path) -> EvalReport:
    """Execute all enabled checks and write the report under `.coding-scaffold/`."""

    root = target.expanduser().resolve()
    config = _load_eval_config(root)
    enabled = config.get("checks", {})
    max_tokens = int(config.get("context_budget_max_tokens", DEFAULT_MAX_CONTEXT_TOKENS))
    if not isinstance(enabled, dict):
        enabled = {}
    warnings: list[str] = []
    checks: list[EvalCheck] = []

    def is_enabled(key: str) -> bool:
        # Default to True if the config doesn't mention the check.
        value = enabled.get(key, True)
        return bool(value)

    if is_enabled("build_command_detected"):
        checks.append(_check_build_signal(root))
    if is_enabled("test_command_detected"):
        checks.append(_check_test_signal(root))
    if is_enabled("lint_command_detected"):
        checks.append(_check_lint_signal(root))
    if is_enabled("agent_instructions_exist"):
        checks.append(_check_agent_instructions(root))
    if is_enabled("policy_exists"):
        checks.append(_check_policy(root))
    if is_enabled("denied_files_configured"):
        checks.append(_check_denied_files(root))
    if is_enabled("pr_template_exists"):
        checks.append(_check_pr_template(root))
    if is_enabled("mcp_policy_exists_if_mcp_detected"):
        checks.append(_check_mcp_policy(root))
    if is_enabled("session_trace_template_exists"):
        checks.append(_check_session_trace(root))
    if is_enabled("context_lint_clean"):
        checks.append(_check_context_lint(root))
    if is_enabled("context_budget_acceptable"):
        checks.append(_check_context_budget(root, max_tokens=max_tokens))

    report = EvalReport(checks=checks, warnings=warnings)
    report_path = root / EVAL_REPORT_RELATIVE
    report_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.write_text(
        json.dumps(report.to_dict(), indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    return report


def load_eval_report(target: Path) -> EvalReport | None:
    """Read the most recent eval report from disk, if any."""

    root = target.expanduser().resolve()
    path = root / EVAL_REPORT_RELATIVE
    if not path.exists():
        return None
    try:
        payload = json.loads(path.read_text(encoding="utf-8-sig"))
    except (OSError, json.JSONDecodeError):
        return None
    checks_payload = payload.get("checks", []) if isinstance(payload, dict) else []
    checks: list[EvalCheck] = []
    for item in checks_payload:
        if not isinstance(item, dict):
            continue
        checks.append(EvalCheck(
            name=str(item.get("name", "")),
            category=str(item.get("category", "")),
            passed=bool(item.get("passed", False)),
            message=str(item.get("message", "")),
            detail=item.get("detail") if isinstance(item.get("detail"), dict) else None,
        ))
    warnings = payload.get("warnings", []) if isinstance(payload, dict) else []
    if not isinstance(warnings, list):
        warnings = []
    return EvalReport(checks=checks, warnings=[str(w) for w in warnings])


