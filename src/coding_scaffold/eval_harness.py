def _check_build_signal(root: Path) -> EvalCheck:
    detected = [f for f in BUILD_SIGNAL_FILES if (root / f).exists()]
    return EvalCheck(
        name="build_command_detected",
        category="basics",
        passed=bool(detected),
        message=(
            f"Detected build signal file(s): {', '.join(detected)}"
            if detected
            else "No build signal file found (looked for pyproject.toml, package.json, "
            "Cargo.toml, go.mod, Gemfile, composer.json, CMakeLists.txt, Makefile)."
        ),
        detail={"detected": detected} if detected else None,
    )


def _check_test_signal(root: Path) -> EvalCheck:
    # Reuse the context-lint heuristic: if `missing-build-test-commands` would NOT fire,
    # we consider the test signal detected. This couples the two checks intentionally.
    report = lint_context(root)
    missing = [f for f in report.findings if f.rule == "missing-build-test-commands"]
    passed = not missing
    if passed:
        message = (
            "Project type detected and a recognizable test command is mentioned in the "
            "agent-context files."
        )
    else:
        message = missing[0].message
    return EvalCheck(
        name="test_command_detected",
        category="basics",
        passed=passed,
        message=message,
    )


def _check_lint_signal(root: Path) -> EvalCheck:
    config_hits = [f for f in LINT_SIGNAL_FILES if (root / f).exists()]
    # pyproject.toml only counts when it actually has a ruff/black section. Quick heuristic.
    if "pyproject.toml" in config_hits:
        try:
            text = (root / "pyproject.toml").read_text(encoding="utf-8-sig").lower()
            if "[tool.ruff" not in text and "[tool.black" not in text and "[tool.mypy" not in text:
                config_hits = [f for f in config_hits if f != "pyproject.toml"]
        except OSError:
            pass
    passed = bool(config_hits)
    return EvalCheck(
        name="lint_command_detected",
        category="basics",
        passed=passed,
        message=(
            f"Lint configuration found: {', '.join(config_hits)}"
            if passed
            else "No lint configuration file detected (ruff/eslint/rubocop/rustfmt)."
        ),
        detail={"detected": config_hits} if config_hits else None,
    )


def _check_agent_instructions(root: Path) -> EvalCheck:
    candidates = ["AGENTS.md", "CLAUDE.md", ".coding-scaffold/AGENTS.md", "llms.txt"]
    found = [c for c in candidates if (root / c).exists()]
    return EvalCheck(
        name="agent_instructions_exist",
        category="guidance",
        passed=bool(found),
        message=(
            f"Found agent-instruction file(s): {', '.join(found)}"
            if found
            else "No AGENTS.md, CLAUDE.md, or llms.txt found at the project root."
        ),
        detail={"found": found} if found else None,
    )


def _check_policy(root: Path) -> EvalCheck:
    policy_dir = root / ".coding-scaffold" / "policy"
    if not policy_dir.exists():
        return EvalCheck(
            name="policy_exists",
            category="governance",
            passed=False,
            message="No `.coding-scaffold/policy/` directory found. Run `coding-scaffold policy`.",
        )
    contents = [p.name for p in policy_dir.iterdir() if p.is_file()]
    return EvalCheck(
        name="policy_exists",
        category="governance",
        passed=bool(contents),
        message=(
            f"Policy pack present: {', '.join(sorted(contents))}"
            if contents
            else "Policy directory exists but is empty."
        ),
        detail={"files": sorted(contents)} if contents else None,
    )


def _check_denied_files(root: Path) -> EvalCheck:
    path = root / PERMISSIONS_RELATIVE
    if not path.exists():
        return EvalCheck(
            name="denied_files_configured",
            category="governance",
            passed=False,
            message=(
                "No `.coding-scaffold/agent-permissions.json` found. Run "
                "`coding-scaffold permissions write`."
            ),
        )
    try:
        payload = json.loads(path.read_text(encoding="utf-8-sig"))
    except (OSError, json.JSONDecodeError) as exc:
        return EvalCheck(
            name="denied_files_configured",
            category="governance",
            passed=False,
            message=f"agent-permissions.json is not valid JSON: {exc}",
        )
    fs = payload.get("filesystem", {}) if isinstance(payload, dict) else {}
    deny = fs.get("deny", []) if isinstance(fs, dict) else []
    passed = bool(deny)
    return EvalCheck(
        name="denied_files_configured",
        category="governance",
        passed=passed,
        message=(
            f"agent-permissions.json declares {len(deny)} deny pattern(s)."
            if passed
            else "agent-permissions.json has an empty deny list."
        ),
        detail={"deny_count": len(deny) if isinstance(deny, list) else 0},
    )


def _check_pr_template(root: Path) -> EvalCheck:
    path = root / PR_TEMPLATE_RELATIVE
    passed = path.exists()
    # Accept any PR template in the directory, not just our generated one.
    pr_dir = root / ".github" / "PULL_REQUEST_TEMPLATE"
    has_any = pr_dir.exists() and any(pr_dir.iterdir())
    if not passed and has_any:
        passed = True
    return EvalCheck(
        name="pr_template_exists",
        category="reviewability",
        passed=passed,
        message=(
            "PR template found under .github/PULL_REQUEST_TEMPLATE/."
            if passed
            else "No PR template found. Run `coding-scaffold pr-template init`."
        ),
    )


def _check_mcp_policy(root: Path) -> EvalCheck:
    # Detect MCP configs.
    mcp_in_use = any((root / rel_path).exists() for rel_path, _key in MCP_CONFIG_SOURCES)
    policy_present = (root / MCP_POLICY_RELATIVE).exists()
    if not mcp_in_use:
        return EvalCheck(
            name="mcp_policy_exists_if_mcp_detected",
            category="governance",
            passed=True,
            message="No MCP configuration detected; check skipped.",
            detail={"mcp_detected": False},
        )
    return EvalCheck(
        name="mcp_policy_exists_if_mcp_detected",
        category="governance",
        passed=policy_present,
        message=(
            "MCP configuration detected and `.coding-scaffold/mcp-policy.json` exists."
            if policy_present
            else "MCP configuration detected but no `.coding-scaffold/mcp-policy.json`. "
            "Run `coding-scaffold mcp policy init`."
        ),
        detail={"mcp_detected": True, "policy_present": policy_present},
    )


def _check_session_trace(root: Path) -> EvalCheck:
    sessions = root / SESSIONS_DIR
    template = root / ".coding-scaffold" / "knowledge" / "sessions" / "session-template.md"
    has_session = sessions.exists() or template.exists()
    return EvalCheck(
        name="session_trace_template_exists",
        category="reviewability",
        passed=has_session,
        message=(
            "Session trace location available."
            if has_session
            else "No `.coding-scaffold/sessions/` or knowledge/sessions/session-template.md "
            "found. Run `coding-scaffold session init` for ad-hoc traces or "
            "`coding-scaffold knowledge create` for the curated template."
        ),
    )


def _check_context_lint(root: Path) -> EvalCheck:
    report = lint_context(root)
    if not report.scanned_files:
        return EvalCheck(
            name="context_lint_clean",
            category="quality",
            passed=False,
            message="No agent-context files found to lint.",
        )
    return EvalCheck(
        name="context_lint_clean",
        category="quality",
        passed=report.error_count == 0,
        message=(
            f"context lint: {report.error_count} error / {report.warning_count} warning / "
            f"{report.info_count} info across {len(report.scanned_files)} file(s)."
        ),
        detail={
            "errors": report.error_count,
            "warnings": report.warning_count,
            "info": report.info_count,
            "files": list(report.scanned_files),
        },
    )


def _check_context_budget(root: Path, *, max_tokens: int) -> EvalCheck:
    budget = inspect_context_budget(root, source="knowledge", max_tokens=max_tokens)
    tokens = int(budget.tokens_estimate)
    passed = tokens <= max_tokens
    return EvalCheck(
        name="context_budget_acceptable",
        category="quality",
        passed=passed,
        message=(
            f"Knowledge context ~{tokens} tokens (budget: {max_tokens})."
            if passed
            else f"Knowledge context ~{tokens} tokens exceeds budget of {max_tokens}."
        ),
        detail={"tokens": tokens, "max_tokens": max_tokens},
    )


